<?php

namespace frontend\controllers;

use common\models\CommentRecipe;
use common\models\Favourite;
use common\models\ProductRecipe;
use common\models\Recipe;
use common\models\ReviewRecipe;
use common\models\User;
use yii\data\ActiveDataProvider;
use yii\web\Controller;
use yii\web\NotFoundHttpException;
use yii\filters\VerbFilter;

/**
 * UserController implements the CRUD actions for User model.
 */
class UserController extends Controller
{
    /**
     * @inheritDoc
     */
    public function behaviors()
    {
        return array_merge(
            parent::behaviors(),
            [
                'verbs' => [
                    'class' => VerbFilter::class,
                    'actions' => [
                        'delete' => ['POST'],
                    ],
                ],
            ]
        );
    }

    /**
     * Lists all User models.
     *
     * @return string
     */
    public function actionIndex()
    {
        $dataProvider = new ActiveDataProvider([
            'query' => User::find(),
            /*
            'pagination' => [
                'pageSize' => 50
            ],
            'sort' => [
                'defaultOrder' => [
                    'id' => SORT_DESC,
                ]
            ],
            */
        ]);

        return $this->render('index', [
            'dataProvider' => $dataProvider,
        ]);
    }

    /**
     * Displays a single User model.
     * @param int $id ID
     * @return string
     * @throws NotFoundHttpException if the model cannot be found
     */
    public function actionView($id)
    {
        $user_recipe = new ActiveDataProvider(['query' => Recipe::find()->where(['user_id' => $id])->with('productRecipes.product')]);
        $user_review = new ActiveDataProvider(['query' => ReviewRecipe::find()->where(['user_id' => $id])]);
        $user_comment = new ActiveDataProvider(['query' => CommentRecipe::find()->where(['user_id' => $id])]);
        $user_favourite = new ActiveDataProvider(['query' => Favourite::find()->where(['user_id' => $id])]);
        $count_recipe = Recipe::find()->where(['user_id' => $id])->count();
        $count_review = ReviewRecipe::find()->where(['user_id' => $id])->count();
        $count_comment = CommentRecipe::find()->where(['user_id' => $id])->count();
        $count_favourite = Favourite::find()->where(['user_id' => $id])->count();

        return $this->render('view', [
            'model' => $this->findModel($id),
            'user_recipe' => $user_recipe,
            'user_review' => $user_review,
            'user_comment' => $user_comment,
            'user_favourite' => $user_favourite,
            'count_recipe' => $count_recipe,
            'count_review' => $count_review,
            'count_comment' => $count_comment,
            'count_favourite' => $count_favourite,
        ]);
    }

    /**
     * Creates a new User model.
     * If creation is successful, the browser will be redirected to the 'view' page.
     * @return string|\yii\web\Response
     */
    public function actionCreate()
    {
        $model = new User();

        if ($this->request->isPost) {
            if ($model->load($this->request->post()) && $model->save()) {
                return $this->redirect(['view', 'id' => $model->id]);
            }
        } else {
            $model->loadDefaultValues();
        }

        return $this->render('create', [
            'model' => $model,
        ]);
    }

    /**
     * Updates an existing User model.
     * If update is successful, the browser will be redirected to the 'view' page.
     * @param int $id ID
     * @return string|\yii\web\Response
     * @throws NotFoundHttpException if the model cannot be found
     */
    public function actionUpdate($id)
    {
        $model = $this->findModel($id);

        if ($this->request->isPost && $model->load($this->request->post()) && $model->save()) {
            return $this->redirect(['view', 'id' => $model->id]);
        }

        return $this->render('update', [
            'model' => $model,
        ]);
    }

    /**
     * Deletes an existing User model.
     * If deletion is successful, the browser will be redirected to the 'index' page.
     * @param int $id ID
     * @return \yii\web\Response
     * @throws NotFoundHttpException if the model cannot be found
     */
    public function actionDelete($id)
    {
        $this->findModel($id)->delete();

        return $this->redirect(['index']);
    }

    /**
     * Finds the User model based on its primary key value.
     * If the model is not found, a 404 HTTP exception will be thrown.
     * @param int $id ID
     * @return User the loaded model
     * @throws NotFoundHttpException if the model cannot be found
     */
    protected function findModel($id)
    {
        if (($model = User::findOne(['id' => $id])) !== null) {
            return $model;
        }

        throw new NotFoundHttpException('The requested page does not exist.');
    }
}
